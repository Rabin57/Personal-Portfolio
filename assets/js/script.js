// Array of job titles to display
const jobTitles = ["Software Developer", "Software Tester", "Web Developer"];
let currentIndex = 0; // Track the current job title index
const jobTitleElement = document.getElementById("job-title");

// Configuration: adjust these speeds to control the animation
const typingSpeed = 100;  // Speed to type each letter (ms)
const erasingSpeed = 80;  // Speed to erase each letter (ms)
const delayBetweenTitles = 1000; // Delay between typing and erasing (ms)

// Function to type the current job title letter by letter
function typeTitle(title, callback) {
    let charIndex = 0;

    const typingInterval = setInterval(() => {
        if (charIndex < title.length) {
            jobTitleElement.textContent += title[charIndex]; // Add one letter
            charIndex++;
        } else {
            clearInterval(typingInterval); // Stop typing
            setTimeout(callback, delayBetweenTitles); // Delay before erasing
        }
    }, typingSpeed);
}

// Function to erase the current job title letter by letter
function eraseTitle(callback) {
    const erasingInterval = setInterval(() => {
        if (jobTitleElement.textContent.length > 0) {
            jobTitleElement.textContent = jobTitleElement.textContent.slice(0, -1); // Remove one letter
        } else {
            clearInterval(erasingInterval); // Stop erasing
            setTimeout(callback, 500); // Small delay before typing the next title
        }
    }, erasingSpeed);
}

// Main function to handle the typing and erasing loop
function loopTitles() {
    const currentTitle = jobTitles[currentIndex];
    typeTitle(currentTitle, () => {
        eraseTitle(() => {
            currentIndex = (currentIndex + 1) % jobTitles.length; // Move to the next title
            loopTitles(); // Repeat the process
        });
    });
}

// Start the title loop on page load
loopTitles();


